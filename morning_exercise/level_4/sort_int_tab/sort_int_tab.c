void sort_int_tab(int *tab, unsigned int size)
{
		unsigned int		i;
		int		swap;

		i = 0;
		size--;
		while (i < size)
		{
				if (tab[i] > tab[i + 1])
				{
						swap = tab[i];
						tab[i] = tab[i + 1];
						tab[i + 1] = swap;
						i = 0;
				}
				else
						i++;
		}
}
